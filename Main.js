var mago = new Mago("Gandalf");
var luchador = new Luchador("Conan");
var arquero = new Arquero("Legolas");
// Acciones del Mago
mago.atacar();
mago.lanzarHechizo();
mago.evolucionar(); // El Mago puede evolucionar
// El mago encuentra una caja mágica y aprende una nueva habilidad
mago.aprenderHabilidad("Explosión mágica");
// Acciones del Luchador
luchador.atacar();
luchador.golpear();
luchador.evolucionar(); // El Luchador puede evolucionar
// El luchador encuentra una caja mágica y aprende una nueva habilidad
luchador.aprenderHabilidad("Puño de hierro");
// Acciones del Arquero
arquero.atacar();
arquero.dispararFlecha();
arquero.evolucionar(); // El Arquero puede evolucionar
// El arquero encuentra una caja mágica y aprende una nueva habilidad
arquero.aprenderHabilidad("Lluvia de flechas");
